package net.fortuna.ical4j.model.property.immutable;

import net.fortuna.ical4j.model.Parameter;
import net.fortuna.ical4j.model.Property;
import net.fortuna.ical4j.model.property.Action;
import net.fortuna.ical4j.model.property.ImmutableProperty;

/**
 * @author Ben Fortuna An immutable instance of Action.
 */
public final class ImmutableAction extends Action implements ImmutableProperty {

    /**
     * Constant actions for playing an audible sound.
     */
    public static final Action AUDIO;
    /**
     * Constant action for displaying a visible notification.
     */
    public static final Action DISPLAY;
    /**
     * Constant action for sending an email.
     */
    public static final Action EMAIL;
    /**
     * Constant action for a procedure.
     */
    public static final Action PROCEDURE;

    private static final long serialVersionUID = -2752235951243969905L;

    static {
        AUDIO = new ImmutableAction(VALUE_AUDIO);
        DISPLAY = new ImmutableAction(VALUE_DISPLAY);
        EMAIL = new ImmutableAction(VALUE_EMAIL);
        PROCEDURE = new ImmutableAction(VALUE_PROCEDURE);
    }

    /**
     * @param value
     */
    public ImmutableAction(final String value) {
        super(value);
    }

    @Override
    public <T extends Property> T add(Parameter parameter) {
        return ImmutableProperty.super.add(parameter);
    }

    @Override
    public <T extends Property> T remove(Parameter parameter) {
        return ImmutableProperty.super.remove(parameter);
    }

    @Override
    public <T extends Property> T removeAll(String... parameterName) {
        return ImmutableProperty.super.removeAll(parameterName);
    }

    @Override
    public <T extends Property> T replace(Parameter parameter) {
        return ImmutableProperty.super.replace(parameter);
    }

    @Override
    public void setValue(final String aValue) {
        ImmutableProperty.super.setValue(aValue);
    }
}
