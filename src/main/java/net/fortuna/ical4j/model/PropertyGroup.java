package net.fortuna.ical4j.model;

import org.jetbrains.annotations.NotNull;

import java.util.List;
import java.util.stream.Collectors;

/**
 * Support for operations applicable to a group of properties. Currently, property grouping is only supported in
 * the vCard specification.
 */
public class PropertyGroup implements PropertyListAccessor {

    private final PropertyList propertyList;

    public PropertyGroup(List<Property> properties, @NotNull String prefix) {
        this.propertyList = new PropertyList(properties.stream().filter(p -> prefix.equals(p.getPrefix()))
                .collect(Collectors.toList()));
    }

    @Override
    public PropertyList getPropertyList() {
        return propertyList;
    }
}
